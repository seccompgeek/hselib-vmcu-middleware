/// LPUART hardware instance number used by this middleware.
pub const LPUART_CHANNEL: u8 = 3;

/// FlexIO channel configured as UART transmitter.
pub const FLEXIO_TX_CHANNEL: u8 = 0;

/// FlexIO channel configured as UART receiver.
pub const FLEXIO_RX_CHANNEL: u8 = 1;

/// Busy-poll iteration limit. Prevents infinite loops on hardware faults.
const POLL_TIMEOUT: u32 = 0x00FF_FFFF;

// =============================================================================
// Public API types
// =============================================================================

/// Selects the physical UART backend for a [`send_data`] or [`receive_data`] call.
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum UartBackend {
    /// Operate via the LPUART peripheral (instance [`LPUART_CHANNEL`]).
    Lpuart,
    /// Operate via the FlexIO UART transmitter channel ([`FLEXIO_TX_CHANNEL`]).
    FlexioTx,
    /// Operate via the FlexIO UART receiver channel ([`FLEXIO_RX_CHANNEL`]).
    FlexioRx,
}

/// Errors returned by the communication-middleware layer.
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum CommError {
    /// A zero-length buffer was passed to the function.
    InvalidArgument,
    /// The LPUART driver returned a non-success, non-busy status.
    LpuartError(LpuartStatus),
    /// The FlexIO UART driver returned a non-success, non-busy status.
    FlexioError(FlexioStatus),
    /// The busy-poll loop reached [`POLL_TIMEOUT`] iterations without completing.
    PollTimeout,
    /// [`send_data`] was called with [`UartBackend::FlexioRx`] (receive-only),
    /// or [`receive_data`] was called with [`UartBackend::FlexioTx`] (transmit-only).
    WrongDirection,
}

// =============================================================================
// Initialization / deinitialization
// =============================================================================

/// Initialises the LPUART instance and both FlexIO UART channels.
///
/// Must be called once — before any [`send_data`] or [`receive_data`] call —
/// after the clock and pin-mux layers are already configured.  Call [`deinit`]
/// when the middleware is no longer needed.
///
/// The config pointers must remain valid for the full lifetime of the driver;
/// they are typically the `extern` constants generated by the BSW configuration
/// tool (e.g. `&Lpuart_Uart_Ip_xHwConfigPB_3_VS_0`).
///
/// # Safety
///
/// * `lpuart_config` must point to a valid `Lpuart_Uart_Ip_UserConfigType`.
/// * `flexio_tx_config` / `flexio_rx_config` must each point to a valid
///   `Flexio_Uart_Ip_UserConfigType`.
/// * Calling `init` more than once without an intervening `deinit` is
///   undefined behaviour in the underlying C driver.
pub unsafe fn init(
    lpuart_config: *const LpuartUserConfig,
    flexio_tx_config: *const FlexioUserConfig,
    flexio_rx_config: *const FlexioUserConfig,
) {
    Lpuart_Uart_Ip_Init(LPUART_CHANNEL, lpuart_config);
    Flexio_Uart_Ip_Init(FLEXIO_TX_CHANNEL, flexio_tx_config);
    Flexio_Uart_Ip_Init(FLEXIO_RX_CHANNEL, flexio_rx_config);
}

/// Deinitialises the LPUART instance and both FlexIO UART channels.
///
/// After this call the peripherals are disabled and all driver state is reset.
/// Non-fatal status codes from the underlying driver are silently discarded.
///
/// # Safety
///
/// Must only be called after a successful [`init`].
pub unsafe fn deinit() {
    let _ = Lpuart_Uart_Ip_Deinit(LPUART_CHANNEL);
    let _ = Flexio_Uart_Ip_Deinit(FLEXIO_RX_CHANNEL);
    let _ = Flexio_Uart_Ip_Deinit(FLEXIO_TX_CHANNEL);
}

// =============================================================================
// Send
// =============================================================================

/// Transmits `buf` over `backend` and blocks until the transfer is complete.
///
/// Steps performed:
/// 1. Calls the driver's `AsyncSend` to kick off the non-blocking transfer.
/// 2. Spins on `GetTransmitStatus` / `GetStatus` until `SUCCESS` is returned
///    or the iteration limit ([`POLL_TIMEOUT`]) is exceeded.
///
/// # Errors
///
/// * [`CommError::InvalidArgument`] — `buf` is empty.
/// * [`CommError::WrongDirection`] — `backend` is [`UartBackend::FlexioRx`].
/// * [`CommError::LpuartError`] / [`CommError::FlexioError`] — driver failure.
/// * [`CommError::PollTimeout`] — hardware did not finish within the timeout.
pub fn send_data(backend: UartBackend, buf: &[u8]) -> Result<(), CommError> {
    if buf.is_empty() {
        return Err(CommError::InvalidArgument);
    }
    match backend {
        UartBackend::Lpuart   => send_lpuart(buf),
        UartBackend::FlexioTx => send_flexio(buf),
        UartBackend::FlexioRx => Err(CommError::WrongDirection),
    }
}

/// LPUART transmit path: async-send + poll-until-done.
fn send_lpuart(buf: &[u8]) -> Result<(), CommError> {
    let status = unsafe {
        Lpuart_Uart_Ip_AsyncSend(LPUART_CHANNEL, buf.as_ptr(), buf.len() as u32)
    };
    if status != LpuartStatus::Success {
        return Err(CommError::LpuartError(status));
    }

    let mut remaining: u32 = 0;
    let mut timeout = POLL_TIMEOUT;
    loop {
        let poll = unsafe { Lpuart_Uart_Ip_GetTransmitStatus(LPUART_CHANNEL, &mut remaining) };
        match poll {
            LpuartStatus::Success => return Ok(()),
            LpuartStatus::Busy => {
                timeout = timeout.checked_sub(1).ok_or(CommError::PollTimeout)?;
            }
            err => return Err(CommError::LpuartError(err)),
        }
    }
}

/// FlexIO TX transmit path: async-send + poll-until-done.
fn send_flexio(buf: &[u8]) -> Result<(), CommError> {
    let status = unsafe {
        Flexio_Uart_Ip_AsyncSend(FLEXIO_TX_CHANNEL, buf.as_ptr(), buf.len() as u32)
    };
    if status != FlexioStatus::Success {
        return Err(CommError::FlexioError(status));
    }

    let mut remaining: u32 = 0;
    let mut timeout = POLL_TIMEOUT;
    loop {
        let poll = unsafe { Flexio_Uart_Ip_GetStatus(FLEXIO_TX_CHANNEL, &mut remaining) };
        match poll {
            FlexioStatus::Success => return Ok(()),
            FlexioStatus::Busy => {
                timeout = timeout.checked_sub(1).ok_or(CommError::PollTimeout)?;
            }
            err => return Err(CommError::FlexioError(err)),
        }
    }
}

