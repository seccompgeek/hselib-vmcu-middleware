//! communication_middleware — LPUART / FlexIO UART middleware for the S32K344.
//!
//! This module provides safe, idiomatic Rust wrappers around the NXP RTD
//! `Lpuart_Uart_Ip` and `Flexio_Uart_Ip` IP-layer drivers that are declared in
//! [`crate::ffi`].
//!
//! # Architecture
//!
//! ```text
//!  ┌──────────────────────────────────────────┐
//!  │              Application code             │
//!  └─────────────────┬────────────────────────┘
//!                    │  safe API
//!  ┌─────────────────▼────────────────────────┐
//!  │        communication_middleware           │
//!  │  init / deinit / send_data / receive_data │
//!  └─────────────────┬────────────────────────┘
//!                    │  unsafe FFI
//!  ┌─────────────────▼────────────────────────┐
//!  │  ffi.rs   ──   C RTD driver layer        │
//!  │  Lpuart_Uart_Ip_*  /  Flexio_Uart_Ip_*   │
//!  └──────────────────────────────────────────┘
//! ```
//!
//! # Hardware channel assignments
//!
//! These match the definitions used in the
//! `LpuartFlexio_Uart_Ip_Example_S32K344` reference project:
//!
//! | Constant            | Value | Description                |
//! |---------------------|-------|----------------------------|
//! | `LPUART_CHANNEL`    | 3     | LPUART instance 3          |
//! | `FLEXIO_TX_CHANNEL` | 0     | FlexIO UART transmit ch. 0 |
//! | `FLEXIO_RX_CHANNEL` | 1     | FlexIO UART receive  ch. 1 |
//!
//! # Adding support for new channels
//!
//! 1. Extend [`UartBackend`] with the new variant.
//! 2. Add a corresponding arm to the `match` in [`send_data`] / [`receive_data`].
//! 3. Declare any additional C functions in `ffi.rs`.

