// Secure receive
// =============================================================================

/// Receives a secure frame, verifies its AES-CMAC-128 tag, then decrypts the
/// ciphertext into `plaintext_out`.
///
/// The caller must supply the expected ciphertext length (`expected_ct_len`),
/// which equals the plaintext length used by the sender (no padding is added).
/// The function receives exactly `expected_ct_len + 32` bytes from `backend`.
///
/// **Decryption happens only after the CMAC is verified** (encrypt-then-MAC).
///
/// # Parameters
///
/// * `backend`         — UART backend to receive from.
/// * `plaintext_out`   — output buffer; must be ≥ `expected_ct_len` bytes.
/// * `expected_ct_len` — expected ciphertext/plaintext length in bytes;
///                       must be a non-zero multiple of 16 and ≤ [`MAX_SECURE_PLAINTEXT`].
///
/// # Errors
///
/// * [`SecureCommError::NotInitialized`] — [`secure_init`] was not called.
/// * [`SecureCommError::InvalidLength`]  — invalid `expected_ct_len` or
///   `plaintext_out` is too small.
/// * [`SecureCommError::Transport`]      — UART receive error.
/// * [`SecureCommError::MacVerifyFailed`] — CMAC mismatch; frame rejected.
/// * [`SecureCommError::CipherFailed`]   — HSE AES decrypt error.
///
/// # Returns
///
/// On success, returns `Ok(expected_ct_len)` (plaintext byte count written to
/// `plaintext_out`).
pub fn secure_receive(
    backend: UartBackend,
    plaintext_out: &mut [u8],
    expected_ct_len: usize,
) -> Result<usize, SecureCommError> {
    if expected_ct_len == 0
        || expected_ct_len % AES_BLOCK_SIZE != 0
        || expected_ct_len > MAX_SECURE_PLAINTEXT
        || plaintext_out.len() < expected_ct_len
    {
        return Err(SecureCommError::InvalidLength);
    }

    let cipher_handle = CIPHER_KEY_HANDLE.load(Ordering::Relaxed);
    let mac_handle = MAC_KEY_HANDLE.load(Ordering::Relaxed);
    if cipher_handle == INVALID_KEY_HANDLE || mac_handle == INVALID_KEY_HANDLE {
        return Err(SecureCommError::NotInitialized);
    }

    let frame_len = IV_SIZE + expected_ct_len + CMAC_TAG_SIZE;
    let mut frame = [0u8; IV_SIZE + MAX_SECURE_PLAINTEXT + CMAC_TAG_SIZE];

    // 1. Receive the full frame.
    receive_data(backend, &mut frame[..frame_len]).map_err(SecureCommError::Transport)?;

    // 2. Verify CMAC over frame[0..IV_SIZE+ct_len]  (IV || ciphertext).
    //    The tag sits at frame[IV_SIZE+ct_len..frame_len].
    //    We verify BEFORE decrypting to prevent decryption-oracle attacks.
    let tag_arr = <&[u8; CMAC_TAG_SIZE]>::try_from(
        &frame[IV_SIZE + expected_ct_len..IV_SIZE + expected_ct_len + CMAC_TAG_SIZE],
    ).unwrap();
    hse::aes_cmac_verify(mac_handle, &frame[..IV_SIZE + expected_ct_len], tag_arr)
        .map_err(|_| SecureCommError::MacVerifyFailed)?;

    // 3. Decrypt frame[IV_SIZE..IV_SIZE+ct_len] → plaintext_out.
    //    IV and ciphertext are both immutable borrows of frame; plaintext_out
    //    is a separate external buffer — no aliasing concern.
    let iv_arr = <&[u8; IV_SIZE]>::try_from(&frame[..IV_SIZE]).unwrap();
    let ct = &frame[IV_SIZE..IV_SIZE + expected_ct_len];
    hse::aes_cbc_decrypt(cipher_handle, iv_arr, ct, &mut plaintext_out[..expected_ct_len])
        .map_err(SecureCommError::CipherFailed)?;

    Ok(expected_ct_len)
}

