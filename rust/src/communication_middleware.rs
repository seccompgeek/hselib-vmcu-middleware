/// Maximum number of bytes accepted by a single send/receive call.
///
/// Callers that pass `len > MAX_TRANSFER_LEN` receive `false` immediately,
/// preventing an oversized transfer from stalling the UART driver.
pub const MAX_TRANSFER_LEN: u32 = 256;

// ── Helpers ───────────────────────────────────────────────────────────────────

/// Validates a TX pointer+length pair and builds an immutable slice.
///
/// Returns `None` (→ `false` to the C caller) when:
/// * `data` is null
/// * `len == 0` or `len > MAX_TRANSFER_LEN`
#[inline]
fn make_send_slice<'a>(data: *const u8, len: u32) -> Option<&'a [u8]> {
    if data.is_null() || len == 0 || len > MAX_TRANSFER_LEN {
        return None;
    }
    // SAFETY: caller guarantees `data` points to `len` valid, initialised bytes
    // and that the buffer will not be mutated for the duration of the call.
    Some(unsafe { core::slice::from_raw_parts(data, len as usize) })
}

/// Validates an RX pointer+length pair and builds a mutable slice.
///
/// Returns `None` (→ `false` to the C caller) when:
/// * `buf` is null
/// * `len == 0` or `len > MAX_TRANSFER_LEN`
#[inline]
fn make_recv_slice<'a>(buf: *mut u8, len: u32) -> Option<&'a mut [u8]> {
    if buf.is_null() || len == 0 || len > MAX_TRANSFER_LEN {
        return None;
    }
    // SAFETY: caller guarantees `buf` points to `len` writable bytes and that
    // no other reference to the same memory exists during this call.
    Some(unsafe { core::slice::from_raw_parts_mut(buf, len as usize) })
}

// ── send_to_ap / send_to_ap2 ──────────────────────────────────────────────────

/// Transmit `len` bytes from `data` to the application processor over LPUART.
///
/// Returns `true` on success, `false` on any error (null pointer, oversized
/// transfer, or driver failure).
///
/// C declaration (hse_rust.h):
///   `bool send_to_ap(const uint8_t *data, uint32_t len);`
#[no_mangle]
pub extern "C" fn send_to_ap(data: *const u8, len: u32) -> bool {
    let buf = match make_send_slice(data, len) {
        Some(s) => s,
        None => return false,
    };
    send_data(UartBackend::Lpuart, buf).is_ok()
}

/// Transmit `len` bytes from `data` to the application processor, with
/// backend selection.
///
/// When `use_flexio` is `false` (0) the data is sent over LPUART; when
/// `true` (non-zero) it is sent over the FlexIO TX channel.
///
/// Returns `true` on success, `false` on any error.
///
/// C declaration (hse_rust.h):
///   `bool send_to_ap2(const uint8_t *data, uint32_t len, bool use_flexio);`
#[no_mangle]
pub extern "C" fn send_to_ap2(data: *const u8, len: u32, use_flexio: bool) -> bool {
    let buf = match make_send_slice(data, len) {
        Some(s) => s,
        None => return false,
    };
    let backend = if use_flexio {
        UartBackend::FlexioTx
    } else {
        UartBackend::Lpuart
    };
    send_data(backend, buf).is_ok()
}

