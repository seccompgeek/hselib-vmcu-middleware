// The SHE example CBC and CMAC keys from hse_host_wrappers.c
// (SheLoadNVMKey_CBC / SheLoadNVMKey_CMAC).
const SHE_CBC_KEY: [u8; 16] = [
    0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6,
    0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c,
];
const SHE_CMAC_KEY: [u8; 16] = [
    0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6,
    0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c,
];

/// Initialises the secure channel using the constant keys from the SHE
/// example project (`SheLoadNVMKey_CBC` / `SheLoadNVMKey_CMAC` in
/// `hse_host_wrappers.c`).
///
/// This is a convenience wrapper around [`secure_comm_init`] that does not
/// require the caller to supply key bytes — the keys are compiled in.
///
/// Returns `true` on success, `false` if HSE key-import fails.
///
/// C declaration (hse_rust.h):
///   `bool secure_comm_init_she_keys(void);`
#[no_mangle]
pub unsafe extern "C" fn secure_comm_init_she_keys() -> bool {
    secure_init(&SHE_CBC_KEY, &SHE_CMAC_KEY).is_ok()
}

/// Loads AES-128 keys for the secure channel.
///
/// Both `cipher_key` and `mac_key` must point to exactly 16 valid bytes.
/// Returns `true` on success, `false` if HSE key-import fails or a pointer
/// is null.
///
/// C declaration (hse_rust.h):
///   `bool secure_comm_init(const uint8_t *cipher_key, const uint8_t *mac_key);`
#[no_mangle]
pub unsafe extern "C" fn secure_comm_init(cipher_key: *const u8, mac_key: *const u8) -> bool {
    if cipher_key.is_null() || mac_key.is_null() {
        return false;
    }
    // SAFETY: caller guarantees each pointer addresses exactly 16 valid bytes.
    let ck = &*(cipher_key as *const [u8; 16]);
    let mk = &*(mac_key as *const [u8; 16]);
    secure_init(ck, mk).is_ok()
}

/// Encrypts and sends `len` bytes of plaintext over LPUART.
///
/// `len` must be a non-zero multiple of 16 and ≤ 224.
/// Returns `true` on success, `false` on any crypto or transport error.
///
/// C declaration (hse_rust.h):
///   `bool secure_send_to_ap_encrypted(const uint8_t *data, uint32_t len);`
#[no_mangle]
pub extern "C" fn secure_send_to_ap_encrypted(data: *const u8, len: u32) -> bool {
    if data.is_null() || len == 0 || len > MAX_SECURE_PLAINTEXT as u32 {
        return false;
    }
    // SAFETY: pointer is non-null and len > 0 checked above.
    let buf = unsafe { core::slice::from_raw_parts(data, len as usize) };
    secure_send(UartBackend::Lpuart, buf).is_ok()
}

/// Receives a secure frame and decrypts it into `buf`.
///
/// Receives exactly (`expected_ct_len` + 32) bytes, verifies CMAC, then
/// decrypts.  On success `*out_len` is set to `expected_ct_len`.
/// Returns `true` on success, `false` on any error.
///
/// C declaration (hse_rust.h):
///   `bool secure_recv_from_ap_encrypted(
///       uint8_t *buf, uint32_t expected_ct_len, uint32_t *out_len);`
#[no_mangle]
pub extern "C" fn secure_recv_from_ap_encrypted(
    buf: *mut u8,
    expected_ct_len: u32,
    out_len: *mut u32,
) -> bool {
    if buf.is_null()
        || out_len.is_null()
        || expected_ct_len == 0
        || expected_ct_len > MAX_SECURE_PLAINTEXT as u32
    {
        return false;
    }
    // SAFETY: caller guarantees buf points to at least expected_ct_len writable bytes.
    let slice = unsafe { core::slice::from_raw_parts_mut(buf, expected_ct_len as usize) };
    match secure_receive(UartBackend::Lpuart, slice, expected_ct_len as usize) {
        Ok(n) => {
            unsafe { *out_len = n as u32 };
            true
        }
        Err(_) => false,
    }
}
