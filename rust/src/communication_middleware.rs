/// Encrypts `plaintext` with AES-CBC-128, appends a 16-byte AES-CMAC-128 tag,
/// and transmits the resulting frame over `backend`.
///
/// Frame layout: `IV(16) || AES-CBC(plaintext) || CMAC(IV||ciphertext)`
///
/// # Errors
///
/// * [`SecureCommError::NotInitialized`] — [`secure_init`] was not called.
/// * [`SecureCommError::InvalidLength`]  — `plaintext` is empty, not a multiple
///   of 16 bytes, or longer than [`MAX_SECURE_PLAINTEXT`] (224 bytes).
/// * [`SecureCommError::CipherFailed`]   — HSE AES or RNG error.
/// * [`SecureCommError::MacGenerateFailed`] — HSE CMAC error.
/// * [`SecureCommError::Transport`]      — UART send error.
pub fn secure_send(backend: UartBackend, plaintext: &[u8]) -> Result<(), SecureCommError> {
    let n = plaintext.len();
    if n == 0 || n % AES_BLOCK_SIZE != 0 || n > MAX_SECURE_PLAINTEXT {
        return Err(SecureCommError::InvalidLength);
    }

    let cipher_handle = CIPHER_KEY_HANDLE.load(Ordering::Relaxed);
    let mac_handle = MAC_KEY_HANDLE.load(Ordering::Relaxed);
    if cipher_handle == INVALID_KEY_HANDLE || mac_handle == INVALID_KEY_HANDLE {
        return Err(SecureCommError::NotInitialized);
    }

    // Stack-allocate the maximum frame: IV(16) + ciphertext(224) + CMAC(16) = 256 B.
    let mut frame = [0u8; IV_SIZE + MAX_SECURE_PLAINTEXT + CMAC_TAG_SIZE];
    let frame_len = IV_SIZE + n + CMAC_TAG_SIZE;

    // 1. Fill IV with cryptographically strong random bytes.
    hse::get_rng_drg4(&mut frame[..IV_SIZE]).map_err(SecureCommError::CipherFailed)?;

    // 2. Encrypt plaintext → frame[IV_SIZE..IV_SIZE+n].
    //    Copy the IV to a local array so we can hold an immutable borrow on
    //    frame[0..16] while mutably borrowing frame[16..16+n].
    let iv: [u8; IV_SIZE] = frame[..IV_SIZE].try_into().unwrap();
    hse::aes_cbc_encrypt(cipher_handle, &iv, plaintext, &mut frame[IV_SIZE..IV_SIZE + n])
        .map_err(SecureCommError::CipherFailed)?;

    // 3. Compute CMAC over frame[0..IV_SIZE+n]  (IV || ciphertext, contiguous).
    //    split_at_mut gives non-overlapping borrows: mac_input (read) and
    //    tag_dest (write) into the same frame array.
    {
        let (mac_input, tag_dest) = frame.split_at_mut(IV_SIZE + n);
        let tag_arr =
            <&mut [u8; CMAC_TAG_SIZE]>::try_from(&mut tag_dest[..CMAC_TAG_SIZE]).unwrap();
        hse::aes_cmac_generate(mac_handle, mac_input, tag_arr)
            .map_err(SecureCommError::MacGenerateFailed)?;
    }

    // 4. Transmit the complete frame.
    send_data(backend, &frame[..frame_len]).map_err(SecureCommError::Transport)
}

