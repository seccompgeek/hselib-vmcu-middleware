// =============================================================================
// Secure communication layer — AES-CBC-128 encryption + AES-CMAC-128 integrity
// =============================================================================
//
// Wire frame layout (encrypt-then-MAC):
//
//   ┌─────────┬────────────────────────┬──────────┐
//   │ IV 16 B │ Ciphertext  (ct_len B) │ CMAC 16B │
//   └─────────┴────────────────────────┴──────────┘
//
//   ct_len == plaintext_len  (no padding added; caller must align to 16 B)
//   CMAC is computed over the contiguous region:  IV || Ciphertext
//   On-wire byte count:  ct_len + 32
//
// Security properties:
//   • Confidentiality: AES-CBC-128 with a fresh random IV per frame.
//   • Integrity + authenticity: full 128-bit CMAC tag over IV and ciphertext
//     (encrypt-then-MAC prevents decryption oracle attacks).
//   • The CMAC is verified BEFORE decryption; any tampered frame is rejected
//     without ever decrypting a single byte.

const AES_BLOCK_SIZE: usize = 16;
const CMAC_TAG_SIZE: usize = 16;
const IV_SIZE: usize = 16;

/// Maximum plaintext bytes supported per secure call (14 × 16).
///
/// The corresponding on-wire frame is `MAX_SECURE_PLAINTEXT + 32` bytes, which
/// fits exactly within the [`MAX_TRANSFER_LEN`] limit of 256 bytes.
pub const MAX_SECURE_PLAINTEXT: usize = 224;

/// `HSE_INVALID_KEY_HANDLE` sentinel — key slot not yet initialised.
const INVALID_KEY_HANDLE: u32 = 0xFFFF_FFFF;

/// Key handle for the CBC cipher key, stored after a successful [`secure_init`].
static CIPHER_KEY_HANDLE: AtomicU32 = AtomicU32::new(INVALID_KEY_HANDLE);

/// Key handle for the CMAC authentication key, stored after a successful [`secure_init`].
static MAC_KEY_HANDLE: AtomicU32 = AtomicU32::new(INVALID_KEY_HANDLE);

// =============================================================================
// Error type
// =============================================================================

/// Errors that can be returned by the secure-communication layer.
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum SecureCommError {
    /// Underlying UART transport error.
    Transport(CommError),
    /// HSE rejected a key-import request during [`secure_init`].
    KeyLoadFailed(HseError),
    /// HSE AES-CBC encrypt or decrypt operation failed.
    CipherFailed(HseError),
    /// HSE CMAC tag-generation operation failed.
    MacGenerateFailed(HseError),
    /// CMAC tag verification failed: frame was tampered or the wrong key was used.
    MacVerifyFailed,
    /// Plaintext length is zero, not a multiple of 16,
    /// or exceeds [`MAX_SECURE_PLAINTEXT`].
    InvalidLength,
    /// [`secure_init`] has not been called or it returned an error.
    NotInitialized,
}

