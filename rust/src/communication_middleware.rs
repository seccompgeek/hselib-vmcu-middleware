// Initialisation
// =============================================================================

/// Ensures the HSE key catalog is formatted and the software key-handle
/// allocator (HKF) is initialised, then loads the two AES-128 keys required
/// by the secure channel into HSE RAM slots.
///
/// Must be called **once** before any [`secure_send`] or [`secure_receive`] call.
/// Calling it again re-imports the keys into the same slots, which is safe but
/// interrupts any in-flight operation that relies on the old key material.
///
/// # Catalog-setup sequence
///
/// 1. `IsKeyCatalogFormatted()` — checks `HSE_STATUS_INSTALL_OK`.
/// 2. If **not** yet formatted: `FormatKeyCatalogs_()` lays out both the NVM and
///    RAM catalogs using the built-in `HSE_DEMO_*_KEY_CATALOG_CFG` tables and
///    then calls `ParseKeyCatalogs()` internally (initialises the HKF allocator).
/// 3. If **already** formatted: `ParseKeyCatalogs()` is called alone to (re-)
///    initialise the HKF allocator without disturbing the stored NVM keys.
///
/// # Key-slot assignments
///
/// The RAM catalog group-1 contains 10 × AES-128 slots (index from the
/// `HSE_DEMO_RAM_KEY_CATALOG_CFG` definition in `hse_b_catalog_formatting.h`).
///
/// | Purpose       | HSE RAM group | Slot | Handle       | Allowed modes |
/// |---------------|---------------|------|--------------|---------------|
/// | CBC cipher    | 1             | 0    | `0x020100`   | CBC only      |
/// | CMAC          | 1             | 1    | `0x020101`   | Any (MAC use) |
///
/// # Safety
///
/// * `cipher_key` and `mac_key` must each point to exactly 16 valid, initialised
///   bytes for the duration of this call.
/// * This function calls HSE firmware via the MU interface; it must not be
///   called from an interrupt context.
pub unsafe fn secure_init(
    cipher_key: &[u8; 16],
    mac_key: &[u8; 16],
) -> Result<(), SecureCommError> {
    // Step 1: Ensure the HSE key catalog is formatted and HKF is initialised.
    //
    // • IsKeyCatalogFormatted checks the HSE_STATUS_INSTALL_OK status bit.
    // • If the catalog was never formatted (e.g. first boot), FormatKeyCatalogs_
    //   formats both the NVM and RAM catalogs and calls ParseKeyCatalogs() itself.
    // • If already formatted (subsequent boots), only ParseKeyCatalogs() is
    //   called so that existing NVM keys are preserved.
    if IsKeyCatalogFormatted() == 0 {
        let resp = FormatKeyCatalogs_();
        if !resp.is_ok() {
            return Err(SecureCommError::KeyLoadFailed(HseError::Raw(resp.0)));
        }
        // FormatKeyCatalogs_ already calls ParseKeyCatalogs internally.
    } else {
        let resp = ParseKeyCatalogs();
        if !resp.is_ok() {
            return Err(SecureCommError::KeyLoadFailed(HseError::Raw(resp.0)));
        }
    }

    // Step 2: Import AES-CBC cipher key into RAM group-1 slot-0.
    hse::import_plain_sym_key(
        RAM_AES_GROUP1_SLOT0,
        key_flags::ENCRYPT_DECRYPT,
        128,
        1 << 2, // restrict to CBC mode (HSE_KU_AES_BLOCK_MODE_CBC)
        cipher_key,
    ).map_err(SecureCommError::KeyLoadFailed)?;
    CIPHER_KEY_HANDLE.store(RAM_AES_GROUP1_SLOT0, Ordering::Relaxed);

    // Step 3: Import AES-CMAC key into RAM group-1 slot-1.
    // aes_block_mode_mask = 0 means any block mode is allowed; the HSE enforces
    // SIGN/VERIFY usage flags to restrict this key to MAC operations only.
    hse::import_plain_sym_key(
        RAM_AES_GROUP1_SLOT1,
        key_flags::SIGN_VERIFY,
        128,
        0, // any block mode; usage flags restrict to MAC
        mac_key,
    ).map_err(SecureCommError::KeyLoadFailed)?;
    MAC_KEY_HANDLE.store(RAM_AES_GROUP1_SLOT1, Ordering::Relaxed);

    Ok(())
}

